package br.com.jogorobo;

public class Coordenadas {

	private int coordX;
	private int coordY;
	
	public Coordenadas() {
		coordX = 0;
		coordY = 0;
	}

	public int getCoordX() {
		return coordX;
	}

	public void setCoordX(int coordX) {
		this.coordX = coordX;
	}

	public int getCoordY() {
		return coordY;
	}

	public void setCoordY(int coordY) {
		this.coordY = coordY;
	}
	
}
